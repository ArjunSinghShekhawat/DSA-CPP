#include<iostream>
using namespace std;

int main(){

    //range -2^31 to 2^31-1
    int num = 10;
    cout<<"The value of num is :"<<num<<endl;

    //range -2^31 to 2^31-1
    int signed data = -123;
    cout<<"The value of data is :"<<data<<endl;

    //range 0->2^32-1
    int unsigned value = 12;
    cout<<"The value of value is :"<<value<<endl;


    return 0;
}